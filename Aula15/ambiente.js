let num = [9,2,3,6,7]//Mais de um valor para uma variável 

num[6] = 8 //A posição 8 recebe 5
num.push(4)//Acrescenta mais um valor dentro da variável
num.sort()//organiza do menor para o maior

console.log(num)//printa o valor da varialvel
console.log(`O vetor tem ${num.length} posições`)//num.length mostra quantas posições existem na variável "num"
console.log(`O primeiro valor da variável é ${num[0]}`)//mostra quais valores estão implementados nas posições